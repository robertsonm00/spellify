export function getListsForYear(year) {
  if (year === 4) return curriculumLists.filter(l => l.year === 3 || l.year === 4);
  return curriculumLists.filter(l => l.year === year);
}

export function getEnrichedLesson(lessonId) {
  const lesson = curriculumLists.find(l => l.id === lessonId);
  if (!lesson) return null;
  try {
    const { getWordData } = require('../utils/wordLookup');
    return { ...lesson, words: lesson.words.map(e => ({...e, enriched: getWordData(e.word)}))}
;  } catch (_) { return lesson; }
}

export function getEnrichedListsForYear(year) {
  return getListsForYear(year).map(l => getEnrichedLesson(l.id));
}

