/**
 * Find the weak-spot range for a word.
 * Returns { start, end } indices into `word` (end exclusive),
 * or a sensible fallback (middle 2 chars) if no entry exists.
 */
export function getWeakSpot(word) {
  const w = String(word);
  const segment = WEAK_SPOTS[w.toLowerCase()];
  if (segment) {
    const idx = w.toLowerCase().indexOf(segment.toLowerCase());
    if (idx >= 0) return { start: idx, end: idx + segment.length };
  }
  // Fallback: blank the middle 2 chars (or 1 if word is short)
  if (w.length <= 3) return { start: 1, end: 2 };
  const span = w.length >= 6 ? 2 : 1;
  const start = Math.floor((w.length - span) / 2);
  return { start, end: start + span };
}

